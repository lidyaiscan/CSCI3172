/* This function takes in two parameters, length and width, to calculate the area of the rectange */
function areaOfRectangle(length, width) {
    // area of a rectangle: (length)(width)
    let area = length * width;
    console.log("The area of a rectangle with length " + length + " and width " + width + " is: " + area);
    return("The area of a rectangle with length " + length + " and width " + width + " is: " + area);
}